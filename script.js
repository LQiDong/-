let currentLanguage = "zh";
let activeProjectId = null;

const applyLanguage = (language) => {
  currentLanguage = language;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.body.dataset.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[language][key] || element.textContent;
  });
};

const languageToggle = document.querySelector("#languageToggle");
languageToggle.addEventListener("click", () => {
  applyLanguage(currentLanguage === "zh" ? "en" : "zh");
  if (activeProjectId && modal.classList.contains("is-open")) {
    openProject(activeProjectId);
  }
});

document.querySelectorAll(".disabled-link").forEach((link) => {
  link.addEventListener("click", (event) => event.preventDefault());
});

const modal = document.querySelector("#projectModal");
const modalTag = document.querySelector("#modalTag");
const modalTitle = document.querySelector("#modalTitle");
const modalBody = document.querySelector("#modalBody");
const modalPoints = document.querySelector("#modalPoints");

const openProject = (projectId) => {
  const detail = projectDetails[currentLanguage][projectId];
  if (!detail) return;

  activeProjectId = projectId;
  modalTag.textContent = detail.tag;
  modalTitle.textContent = detail.title;
  modalBody.textContent = detail.body;
  modalPoints.innerHTML = "";

  detail.points.forEach((point) => {
    const item = document.createElement("div");
    item.textContent = point;
    modalPoints.appendChild(item);
  });

  modal.classList.add("is-open");
  modal.setAttribute("aria-hidden", "false");
};

const closeModal = () => {
  activeProjectId = null;
  modal.classList.remove("is-open");
  modal.setAttribute("aria-hidden", "true");
};

document.querySelectorAll(".project-card").forEach((card) => {
  card.addEventListener("click", () => openProject(card.dataset.project));
});

document.querySelectorAll("[data-close-modal]").forEach((control) => {
  control.addEventListener("click", closeModal);
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeModal();
  }
});

const canvas = document.querySelector("#signalCanvas");
const context = canvas.getContext("2d");
const nodes = [];
const nodeCount = 54;

const resizeCanvas = () => {
  canvas.width = window.innerWidth * window.devicePixelRatio;
  canvas.height = window.innerHeight * window.devicePixelRatio;
  context.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
};

const createNodes = () => {
  nodes.length = 0;
  for (let index = 0; index < nodeCount; index += 1) {
    nodes.push({
      x: Math.random() * window.innerWidth,
      y: Math.random() * window.innerHeight,
      radius: Math.random() * 1.8 + 0.8,
      vx: (Math.random() - 0.5) * 0.28,
      vy: (Math.random() - 0.5) * 0.28
    });
  }
};

const animate = () => {
  context.clearRect(0, 0, window.innerWidth, window.innerHeight);
  context.fillStyle = "rgba(61, 240, 220, 0.72)";
  context.strokeStyle = "rgba(61, 240, 220, 0.12)";
  context.lineWidth = 1;

  nodes.forEach((node, index) => {
    node.x += node.vx;
    node.y += node.vy;

    if (node.x < 0 || node.x > window.innerWidth) node.vx *= -1;
    if (node.y < 0 || node.y > window.innerHeight) node.vy *= -1;

    context.beginPath();
    context.arc(node.x, node.y, node.radius, 0, Math.PI * 2);
    context.fill();

    for (let nextIndex = index + 1; nextIndex < nodes.length; nextIndex += 1) {
      const other = nodes[nextIndex];
      const dx = node.x - other.x;
      const dy = node.y - other.y;
      const distance = Math.sqrt(dx * dx + dy * dy);

      if (distance < 135) {
        context.globalAlpha = 1 - distance / 135;
        context.beginPath();
        context.moveTo(node.x, node.y);
        context.lineTo(other.x, other.y);
        context.stroke();
      }
    }

    context.globalAlpha = 1;
  });

  requestAnimationFrame(animate);
};

window.addEventListener("resize", () => {
  resizeCanvas();
  createNodes();
});

applyLanguage("zh");
resizeCanvas();
createNodes();
animate();
